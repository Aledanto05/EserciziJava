package Calcolatrice;

public class Calcolatrice {

    public int sum(int a, int b) {
        return a + b;
    }

    public int sum(int a, int b, int c) {
        return a + b + c;
    }

    public double sum(double a, double b) {
        return a + b;
    }

    public String sum(String s1, String s2) {
        return s1 + s2;
    }
}
